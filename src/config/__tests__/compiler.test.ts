const COND = `{
    "schemaVersion": 1, "kind": "remappr.keymap",
    "meta": { "name": "Cond", "target": "zmk" },
    "keyboard": { "id": "c", "name": "C", "keys": [{"x":0,"y":0}] },
    "layers": [
        { "name": "base", "bindings": ["A"] },
        { "name": "lower", "bindings": ["B"] },
        { "name": "raise", "bindings": ["C"] },
        { "name": "adjust", "bindings": ["D"] }
    ],
    "conditionalLayers": [
        { "ifLayers": ["lower", "raise"], "thenLayer": "adjust" }
    ]
}`

describe('conditional layers', () => {
    it('round-trips through parseKeymap → serialize', () => {
        const cfg = parseKeymap(COND)
        expect(cfg.conditionalLayers).toEqual([
            { ifLayers: ['lower', 'raise'], thenLayer: 'adjust' },
        ])
        const reparsed = parseKeymap(serializeKeymap(cfg))
        expect(reparsed.conditionalLayers).toEqual(cfg.conditionalLayers)
    })

    it('emits a zmk,conditional-layers node with layer indices', () => {
        const dts = String(
            getCompiler('zmk')
                .compile(parseKeymap(COND))
                .files.find((f) => f.filename.endsWith('.keymap'))!.content,
        )
        expect(dts).toContain('compatible = "zmk,conditional-layers"')
        expect(dts).toContain('if-layers = <1 2>;') // lower=1, raise=2
        expect(dts).toContain('then-layer = <3>;') // adjust=3
    })

    it('QMK warns conditional layers are not generated', () => {
        const { diagnostics } = getCompiler('qmk').compile(parseKeymap(COND))
        expect(
            diagnostics.some((d) => /conditional layers/.test(d.message)),
        ).toBe(true)
    })

    it('rejects a conditional layer referencing an unknown layer', () => {
        const bad = COND.replace('"adjust" }', '"nope" }')
        expect(() => parseKeymap(bad)).toThrow(/unknown layer/)
    })
})

