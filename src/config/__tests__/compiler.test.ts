import { readFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { describe, expect, it } from 'vitest'
import {
    getCompiler,
    hasCompiler,
    parseKeymap,
    serializeKeymap,
} from '../index'

const seed = readFileSync(
    fileURLToPath(new URL('../../mock/seed.keymap.json', import.meta.url)),
    'utf8',
)
const config = parseKeymap(seed)

describe('keymap compilers', () => {
    it('registers all three targets', () => {
        expect(hasCompiler('zmk')).toBe(true)
        expect(hasCompiler('qmk')).toBe(true)
        expect(hasCompiler('keychron')).toBe(true)
    })

    it('compiles a ZMK .keymap with the expected behaviors', () => {
        const { files, diagnostics } = getCompiler('zmk').compile(config)
        // keymap + physical-layout overlay
        expect(files).toHaveLength(2)
        const keymap = files.find((f) => f.filename.endsWith('.keymap'))!
        const dts = String(keymap.content)
        expect(dts).toContain('compatible = "zmk,keymap"')
        expect(dts).toContain('&kp Q')
        expect(dts).toContain('&kp LC(C)') // "Ctrl+C" combo string
        // the seed's layer-taps set resolve: prefer-hold, so they get a generated
        // hold-tap node (hold-preferred) instead of the global &lt
        expect(dts).toContain('flavor = "hold-preferred"')
        expect(dts).toMatch(/&ht_0 \d+ /) // layer-tap via generated node
        expect(dts).toContain('&mt LSHFT') // plain mod_tap -> global &mt
        expect(dts).toContain('&mo 2') // momentary adjust (index 2)
        expect(dts).toContain('&bt BT_SEL 0')
        expect(dts).toContain('&rgb_ug RGB_TOG')
        expect(dts).toContain('combo_esc')
        expect(dts).toContain('zmk,behavior-macro')
        expect(dts).toContain('zmk,behavior-tap-dance')
        expect(dts).toContain('&inc_dec_kp') // encoder
        // per_key lighting is unsupported on ZMK -> warn
        expect(
            diagnostics.some(
                (d) => d.level === 'warn' && /per_key/.test(d.message),
            ),
        ).toBe(true)
    })

    it('compiles a QMK keymap.c with the expected keycodes', () => {
        const { files, diagnostics } = getCompiler('qmk').compile(config)
        const c = String(files[0].content)
        expect(files[0].filename).toBe('keymap.c')
        expect(c).toContain('const uint16_t PROGMEM keymaps')
        expect(c).toContain('MO(2)')
        expect(c).toContain('LT(1,')
        expect(c).toContain('LSFT_T(')
        expect(c).toContain('LCTL(') // Ctrl+C
        expect(c).toContain('RGB_MOD') // per_key effect_next is fine on QMK
        // bluetooth output has no standard QMK keycode -> warn
        expect(
            diagnostics.some(
                (d) =>
                    d.level === 'warn' && /output "bluetooth"/.test(d.message),
            ),
        ).toBe(true)
    })

    it('Keychron allows bluetooth output (no output warning)', () => {
        const { diagnostics } = getCompiler('keychron').compile(config)
        expect(
            diagnostics.some((d) => /output "bluetooth"/.test(d.message)),
        ).toBe(false)
    })

    it('emits a ZMK physical-layout .overlay from the geometry', () => {
        const { files } = getCompiler('zmk').compile(config)
        const overlay = files.find((f) => f.filename.endsWith('.overlay'))!
        const o = String(overlay.content)
        expect(o).toContain('#include <physical_layouts.dtsi>')
        expect(o).toContain('compatible = "zmk,physical-layout"')
        expect(o).toContain('&key_physical_attrs')
        // 36 keys -> 36 key_physical_attrs entries
        expect(o.match(/&key_physical_attrs/g)).toHaveLength(36)
    })

    it('emits a geometry-derived matrix-transform wired to the physical-layout', () => {
        const { files, diagnostics } = getCompiler('zmk').compile(config)
        const overlay = files.find((f) => f.filename.endsWith('.overlay'))!
        const o = String(overlay.content)
        expect(o).toContain('#include <dt-bindings/zmk/matrix_transform.h>')
        expect(o).toContain('compatible = "zmk,matrix-transform"')
        expect(o).toContain('transform = <&default_transform>;')
        // one RC() per key, in keymap order
        expect(o.match(/RC\(\d+,\d+\)/g)).toHaveLength(36)
        // honest about the derivation
        expect(
            diagnostics.some((d) => /matrix-transform RC/.test(d.message)),
        ).toBe(true)
    })

    it('lists non-generated board hardware as a checklist in both files', () => {
        const { files } = getCompiler('zmk').compile(config)
        const overlay = String(
            files.find((f) => f.filename.endsWith('.overlay'))!.content,
        )
        expect(overlay).toContain('NOT GENERATED by remappr')
        expect(overlay).toContain('kscan')
        expect(overlay).toContain('pinctrl')
        const keymap = String(
            files.find((f) => f.filename.endsWith('.keymap'))!.content,
        )
        expect(keymap).toContain('NOT GENERATED')
    })

    // pattern-check: skip — regression test data for the per-firmware fallback
    it('drops actions a firmware has no handler for to its own no-op', () => {
        // layer_lock + peripheral are §5.2 Remappr-firmware-only kinds: neither the
        // ZMK nor the QMK handler table carries them, so each compiler must warn +
        // emit its own no-op token (its capability = handler presence) rather than
        // a real binding.
        const cfg = parseKeymap(`{
            "schemaVersion": 1, "kind": "remappr.keymap",
            "meta": { "name": "RO", "target": "zmk" },
            "keyboard": { "id": "ro", "name": "RO",
                "keys": [{"x":0,"y":0},{"x":1,"y":0}] },
            "layers": [
                { "name": "base", "bindings": [
                    { "type": "layer_lock" },
                    { "type": "peripheral", "kind": "encoder", "code": 3 }
                ] },
                { "name": "fn", "bindings": [
                    { "type": "transparent" }, { "type": "transparent" }
                ] }
            ]
        }`)

        const zmk = getCompiler('zmk').compile(cfg)
        const keymap = String(
            zmk.files.find((f) => f.filename.endsWith('.keymap'))!.content,
        )
        expect(keymap).toContain('&none')
        for (const t of ['layer_lock', 'peripheral']) {
            expect(
                zmk.diagnostics.some(
                    (d) =>
                        d.level === 'warn' &&
                        d.message === `"${t}" has no ZMK binding; emitted &none`,
                ),
            ).toBe(true)
        }

        const qmk = getCompiler('qmk').compile(cfg)
        expect(String(qmk.files[0].content)).toContain('KC_NO')
        for (const t of ['layer_lock', 'peripheral']) {
            expect(
                qmk.diagnostics.some(
                    (d) =>
                        d.level === 'warn' &&
                        d.message === `"${t}" has no QMK keycode; emitted KC_NO`,
                ),
            ).toBe(true)
        }
    })
})

